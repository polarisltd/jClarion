package clarion;

import clarion.Main;
import org.jclarion.clarion.Clarion;
import org.jclarion.clarion.ClarionDecimal;
import org.jclarion.clarion.ClarionKey;
import org.jclarion.clarion.ClarionNumber;
import org.jclarion.clarion.ClarionSQLFile;
import org.jclarion.clarion.ClarionString;

@SuppressWarnings("all")
public class Par_k extends ClarionSQLFile
{
	public ClarionNumber u_nr=Clarion.newNumber().setEncoding(ClarionNumber.ULONG);
	public ClarionNumber karte=Clarion.newNumber().setEncoding(ClarionNumber.ULONG);
	public ClarionString tips=Clarion.newString(1);
	public ClarionString grupa=Clarion.newString(7);
	public ClarionString redzamiba=Clarion.newString(1);
	public ClarionString nos_s=Clarion.newString(15);
	public ClarionString nos_p=Clarion.newString(45);
	public ClarionString nos_a=Clarion.newString(12);
	public ClarionString nos_u=Clarion.newString(3);
	public ClarionString nmr_kods=Clarion.newString(22);
	public ClarionString nmr_plus=Clarion.newString(1);
	public ClarionString pvn=Clarion.newString(22);
	public ClarionString pase=Clarion.newString(37);
	public ClarionDecimal atlaide=Clarion.newDecimal(4,1);
	public ClarionNumber nokl_cp=Clarion.newNumber().setEncoding(ClarionNumber.BYTE);
	public ClarionDecimal kred_lim=Clarion.newDecimal(9,2);
	public ClarionDecimal kks231=Clarion.newDecimal(11,2);
	public ClarionDecimal kks531=Clarion.newDecimal(11,2);
	public ClarionString ban_kods=Clarion.newString(15);
	public ClarionString ban_nr=Clarion.newString(34);
	public ClarionString ban_kr=Clarion.newString(11);
	public ClarionString ban_kods2=Clarion.newString(15);
	public ClarionString ban_nr2=Clarion.newString(34);
	public ClarionString ban_kr2=Clarion.newString(11);
	public ClarionString adrese=Clarion.newString(60);
	public ClarionString piezimes=Clarion.newString(25);
	public ClarionString v_kods=Clarion.newString(2);
	public ClarionString kontakts=Clarion.newString(30);
	public ClarionNumber l_datums=Clarion.newNumber().setEncoding(ClarionNumber.LONG);
	public ClarionString ligums=Clarion.newString(30);
	public ClarionNumber l_cdatums=Clarion.newNumber().setEncoding(ClarionNumber.LONG);
	public ClarionDecimal l_summa=Clarion.newDecimal(10,2);
	public ClarionDecimal l_summa1=Clarion.newDecimal(10,2);
	public ClarionString l_fails=Clarion.newString(12);
	public ClarionNumber lt=Clarion.newNumber().setEncoding(ClarionNumber.BYTE);
	public ClarionString tel=Clarion.newString(26);
	public ClarionString fax=Clarion.newString(15);
	public ClarionString email=Clarion.newString(30);
	public ClarionNumber atzime1=Clarion.newNumber().setEncoding(ClarionNumber.BYTE);
	public ClarionNumber atzime2=Clarion.newNumber().setEncoding(ClarionNumber.BYTE);
	public ClarionNumber nokl_dc=Clarion.newNumber().setEncoding(ClarionNumber.BYTE);
	public ClarionString nokl_dc_tips=Clarion.newString(1);
	public ClarionString gnet_flag=Clarion.newString(2);
	public ClarionNumber baits=Clarion.newNumber().setEncoding(ClarionNumber.BYTE);
	public ClarionString acc_kods=Clarion.newString(8);
	public ClarionNumber acc_datums=Clarion.newNumber().setEncoding(ClarionNumber.LONG);
	public ClarionKey nr_key=new ClarionKey("NR_KEY");
	public ClarionKey nos_key=new ClarionKey("NOS_KEY");
	public ClarionKey nos_u_key=new ClarionKey("NOS_U_KEY");
	public ClarionKey karte_key=new ClarionKey("KARTE_KEY");
	public ClarionKey nmr_key=new ClarionKey("NMR_KEY");
	public ClarionKey gnet_key=new ClarionKey("GNET_KEY");

	public Par_k()
	{
		setName(Main.parname);
		setPrefix("PAR");
		setCreate();
		this.addVariable("U_NR",this.u_nr);
		this.addVariable("KARTE",this.karte);
		this.addVariable("TIPS",this.tips);
		this.addVariable("GRUPA",this.grupa);
		this.addVariable("REDZAMIBA",this.redzamiba);
		this.addVariable("NOS_S",this.nos_s);
		this.addVariable("NOS_P",this.nos_p);
		this.addVariable("NOS_A",this.nos_a);
		this.addVariable("NOS_U",this.nos_u);
		this.addVariable("NMR_KODS",this.nmr_kods);
		this.addVariable("NMR_PLUS",this.nmr_plus);
		this.addVariable("PVN",this.pvn);
		this.addVariable("PASE",this.pase);
		this.addVariable("ATLAIDE",this.atlaide);
		this.addVariable("NOKL_CP",this.nokl_cp);
		this.addVariable("KRED_LIM",this.kred_lim);
		this.addVariable("KKS231",this.kks231);
		this.addVariable("KKS531",this.kks531);
		this.addVariable("BAN_KODS",this.ban_kods);
		this.addVariable("BAN_NR",this.ban_nr);
		this.addVariable("BAN_KR",this.ban_kr);
		this.addVariable("BAN_KODS2",this.ban_kods2);
		this.addVariable("BAN_NR2",this.ban_nr2);
		this.addVariable("BAN_KR2",this.ban_kr2);
		this.addVariable("ADRESE",this.adrese);
		this.addVariable("PIEZIMES",this.piezimes);
		this.addVariable("V_KODS",this.v_kods);
		this.addVariable("KONTAKTS",this.kontakts);
		this.addVariable("L_DATUMS",this.l_datums);
		this.addVariable("LIGUMS",this.ligums);
		this.addVariable("L_CDATUMS",this.l_cdatums);
		this.addVariable("L_SUMMA",this.l_summa);
		this.addVariable("L_SUMMA1",this.l_summa1);
		this.addVariable("L_FAILS",this.l_fails);
		this.addVariable("LT",this.lt);
		this.addVariable("TEL",this.tel);
		this.addVariable("FAX",this.fax);
		this.addVariable("EMAIL",this.email);
		this.addVariable("ATZIME1",this.atzime1);
		this.addVariable("ATZIME2",this.atzime2);
		this.addVariable("NOKL_DC",this.nokl_dc);
		this.addVariable("NOKL_DC_TIPS",this.nokl_dc_tips);
		this.addVariable("GNET_FLAG",this.gnet_flag);
		this.addVariable("BAITS",this.baits);
		this.addVariable("ACC_KODS",this.acc_kods);
		this.addVariable("ACC_DATUMS",this.acc_datums);
		nr_key.setNocase().addAscendingField(u_nr);
		this.addKey(nr_key);
		nos_key.setDuplicate().setNocase().setOptional().addAscendingField(nos_a);
		this.addKey(nos_key);
		nos_u_key.setNocase().setOptional().addAscendingField(nos_u);
		this.addKey(nos_u_key);
		karte_key.setNocase().setOptional().addAscendingField(karte);
		this.addKey(karte_key);
		nmr_key.setNocase().setOptional().addAscendingField(nmr_kods).addAscendingField(nmr_plus);
		this.addKey(nmr_key);
		gnet_key.setDuplicate().setNocase().setOptional().addAscendingField(gnet_flag);
		this.addKey(gnet_key);
	}
}
