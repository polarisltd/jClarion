package org.jclarion.clarion.appgen.symbol;

import org.jclarion.clarion.ClarionString;
import org.jclarion.clarion.appgen.loader.Definition;

public class DefinitionObject extends ClarionString 
{
	private Definition definition;

	public DefinitionObject(Definition definition)
	{
		super(definition.render());
		this.definition=definition;
	}


	public Definition getDefinition()
	{
		return definition;
	}
	
}
